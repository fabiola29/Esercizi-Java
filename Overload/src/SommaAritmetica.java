public class SommaAritmetica {

    public int somma(int x, int y) {
        return x + y;
    }

    public float somma(float x, float y) {
        return x + y;
    }

    public float somma(int x, float y) {
        return x + y;
    }

    public float somma(float x, int y) {
        return x + y;
    }

    public double somma(int x, double y, int z) {
        return x + y + z;
    }
}

